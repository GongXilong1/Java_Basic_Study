package Exercise1;

/**
 * 利用 for循环练习:
 * 打印 1~5
 * 打印 5~1
 */
public class ForTest1 {
    public static void main(String[] args) {
        // 打印 1~5
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }

        // 打印5~1
        for(int a = 5; a > 0; a--){
            System.out.println(a);
        }
    }
}

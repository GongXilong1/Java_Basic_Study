package Java_basic1;

public class VariableTest3 {
    public static void main(String[] args){
        // 定义变量记录电影的名字
        String filmName = "送初恋回家";
        // 定义变量记录主演的名字
        String LeadActor_name = "刘鑫 张雨提";
        // 定义变量记录电影的年份
        int fileYear = 2020;
        // 定义变量记录电影的评分
        double filmRating = 9.0;

        // 输出所有变量的值
        System.out.println(filmName);
        System.out.println(LeadActor_name);
        System.out.println(fileYear);
        System.out.println(filmRating);

    }
}
